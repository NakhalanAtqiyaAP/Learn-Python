angka1 = int(input("Masukan angka ke-1 :"))
angka2 = int(input("Masukan angka ke-2 :"))
operasi = input("Pilih operasi(+,-,*,/,%) :")

if operasi == "+" :
 hasil = angka1 + angka2
 print(hasil)
elif operasi == "-" :
 hasil = angka1 - angka2
 print(hasil)
elif operasi == "*" :
 hasil = angka1 * angka2
 print(hasil)
elif operasi == "/" :
 hasil = angka1 / angka2
 print(hasil)
elif operasi == "%" :
 hasil = angka1 % angka2
 print(hasil)
 