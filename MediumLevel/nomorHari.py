input_hari = int(input("Masukan nomor harian : "))

match input_hari:
    case 1:
        print("Hari Senin")
    case 2:
        print("Hari Selasa")
    case 3:
        print("Hari Rabu")
    case 4:
        print("Hari Kamis")
    case 5:
        print("Hari Jum'at")
    case 6:
        print("Hari Sabtu")
    case 7:
        print("Hari Minggu")     